        private readonly ILogger<WeatherForecastController> _logger;
        private readonly UserManager<ApplicationUser> userManager;

        public WeatherForecastController(ILogger<WeatherForecastController> logger, UserManager<ApplicationUser> userManager) : base(userManager)
        {
            _logger = logger;
            this.userManager = userManager;
        }

        [HttpGet]
